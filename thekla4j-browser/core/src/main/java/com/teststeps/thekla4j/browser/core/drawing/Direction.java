package com.teststeps.thekla4j.browser.core.drawing;

/**
 * Enum for pointer move direction
 */
public enum Direction {
  /**
   * Move up
   */
  UP,

  /**
   * Move down
   */
  DOWN,

  /**
   * Move left
   */
  LEFT,

  /**
   * Move right
   */
  RIGHT,

  /**
   * Move diagonally left down
   */
  LEFT_DOWN,

  /**
   * Move diagonally left up
   */
  LEFT_UP,

  /**
   * Move diagonally right down
   */
  RIGHT_DOWN,

  /**
   * Move diagonally right up
   */
  RIGHT_UP
}
