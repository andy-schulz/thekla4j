/**
 * enum representing a key
 */
public enum Key {
  /**
   * The TAB key
   */
  TAB,

  /**
   * The ENTER key
   */
  ENTER,

  /**
   * The ESCAPE key
   */
  ESCAPE,

  /**
   * The SPACE key
   */
  SPACE,

  /**
   * The ARROW DOWN key
   */
  ARROW_DOWN,

  /**
   * The ARROW UP key
   */
  ARROW_UP,

  /**
   * The ARROW LEFT key
   */
  ARROW_LEFT,

  /**
   * The ARROW RIGHT key
   */
  ARROW_RIGHT,

  /**
   * The BACK_SPACE key
   */
  BACK_SPACE,

  /**
   * The DELETE key
   */
  DELETE,

  /**
   * The CONTROL key
   */
  CONTROL,

  /**
   * The SHIFT key
   */
  SHIFT,

  /**
   * The ALT key
   */
  ALT,

  /**
   * The COMMAND key
   */
  COMMAND;
}
